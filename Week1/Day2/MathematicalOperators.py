# different mathematical operators in python

# 1. Addition operator (+)
print(4 + 7)

# 2. Subtraction operator (-)
print(7 - 3)

# 3. Multiplication Operator (*)
print(4 * 6)

# 4. Division operator ( / or // )
print(8 / 4) # this will return a float point integer
print(8 // 4) # this will return an integer value. This also removes all the numbers after the decimal point. For eg: 1.35 will become 1.

# 5. Exponent operator (**)
print(2**4) # this will calculate 2 raised to the power 4